export const getLogoLines = (): string[] => {
	return rawLogo.split("\n").filter((line, index, arr) => {
		if (line.trim().length > 0) return true;
		if (
			index > 0 &&
			index < arr.length - 1 &&
			arr[index - 1].trim().length > 0 &&
			arr[index + 1].trim().length > 0
		)
			return true;
		return false;
	});
};
